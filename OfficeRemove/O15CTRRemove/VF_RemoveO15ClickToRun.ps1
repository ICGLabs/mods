function Temp-FoldersDelete 
{
	$Setlocation = $env:SystemDrive + "\Users"
    Set-Location $Setlocation

	$TestpathOffscrubC2R = $Setlocation + "\*\Appdata\Local\Temp\OffScrubC2R"
	$TestpathOffscrubMSI16 = $Setlocation + "\*\Appdata\Local\Temp\OffScrub_O16msi"
	$TestpathOffscrubMSI15 = $Setlocation + "\*\Appdata\Local\Temp\OffScrub_O15msi"
	$ResultString = $Setlocation + "\*\Appdata\Local\Temp\RemovalResult.txt"

	if(Test-Path $TestpathOffscrubC2R)
	{
    Remove-Item ".\*\Appdata\Local\Temp\OffScrubC2R" -recurse -force 
	}
	elseif(Test-Path $TestpathOffscrubMSI16 )
	{
    Remove-Item ".\*\Appdata\Local\Temp\OffScrub_O16msi" -recurse -force
	}
	elseif(Test-Path  $TestpathOffscrubMSI15)
	{
    Remove-Item ".\*\Appdata\Local\Temp\OffScrub_O15msi" -recurse -force
	}
	if(Test-Path $ResultString)
	{
    Remove-Item ".\*\Appdata\Local\Temp\RemovalResult.txt" -recurse -force 
	}
}

$RemovalResultPath = "$env:temp\RemovalResult.txt"
if(Test-Path $RemovalResultPath)
{
	$RemovalResult = Get-Content -Path $RemovalResultPath -ErrorAction SilentlyContinue
}

if($RemovalResult -ne $null)
{
	if(($RemovalResult.substring($RemovalResult.Length - 7,7) -ieq "SUCCESS") -or ($RemovalResult.substring($RemovalResult.Length - 47,47) -ieq "Uninstall requires a system reboot to complete."))  
	{
		$Verifier = $true
	}
	else
	{
		$Verifier = $false 
	}
}

#Calling function to delete folders from temp
Temp-FoldersDelete 
Update-DiagRootcause -id "RC_RemoveO15ClickToRun" -detected  (-not($Verifier)) 
	






